#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= -2147483648 && n <= 2147483647)
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (-2147483648 == n)
		{
			ft_putchar('2');
			n = 147483648;
		}
		if (n / 10 > 0)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
	else
		ft_putchar('0');
}
